public class Relation {


    /**
     * id : 2
     * name : 母亲
     * call : 的母亲
     */

    private String id;
    private String name;
    private String call;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCall() {
        return call;
    }

    public void setCall(String call) {
        this.call = call;
    }
}
